class Enmienda{
    constructor(codigo, nombre, descripcion, marca, proveedor, precio, categoria, disponibilidad, stock, tipo, formato, despacho){
        this.codigo = codigo;
        this.nombre = nombre;
        this.descripcion = descripcion;
        this.marca = marca;
        this.proveedor = proveedor;
        this.precio = precio;
        this.categoria = categoria;
        this.disponibilidad = disponibilidad;
        this.stock = stock;
        this.tipo = tipo;
        this.formato = formato;
        this.despacho = despacho;
    }
}